bool	is_space(char c)
{
	if (c == 32 || (c >= 9 && c <= 13))
		return (true);
	return (false);
}

/*	==========================================	*/

bool	is_quote(char c, bool)
{
	if (c == '"' || c == '\'')
		return (true);
	return (false);
}

/*	==========================================	*/

bool	is_operator(char c)
{
	if (c == '|' || c == '>' || c == '<')
		return (true);
	return (false);
}

/*	==========================================	*/

int	ft_strlen(char *str)
{
	int	i;

	if (!str)
		return (NULL);
	i = 0;
	while (str[i])
		i++;
	return (i);
}

