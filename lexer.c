#include "minishell.h"

/*	==========================================	*/

bool	is_space(char c)
{
	if (c == 32 || (c >= 9 && c <= 13))
		return (true);
	else
		return (false);
}

/*	==========================================	*/

bool	is_quote(char c)
{
	if (c == '\'' || c == '"')
		return (true);
	else
		return (false);
}

